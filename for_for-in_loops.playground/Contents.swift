//: Playground - noun: a place where people can play

import UIKit


// FOR-IN LOOPS
for i in 1...5 {
    println(i)
}

for i in 1..<5 {
    println(i)
}


// TRADITIONAL FOR LOOPS
for var i = 0; i < 6; ++i {
    println(i)
}

for var i = 0; i < 5; ++i {
    println(i)
}

// ++i vs i++
var i = 0
println("\(i++)")
println(i)
println("\(++i)")
println(i)




// WHILE LOOPS ASSIGNMENT
// #1
var i = 7

while i < 225 {
    println(i)
}
